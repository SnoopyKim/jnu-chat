public class Friend {

    public String email;
    public String photo;
    public String key;

    public Friend() {
        //default constructor
    }

    public String getEmail() {
        return email;
    }
    public String getPhoto() {
        return photo;
    }
    public String getKey() {
        return key;
    }
    public void setEmail(String email) {
        this.email = email;
    }
    public void setPhoto(String photo) {
        this.photo = photo;
    }
    public void setKey(String key) {
        this.key = key;
    }

}
